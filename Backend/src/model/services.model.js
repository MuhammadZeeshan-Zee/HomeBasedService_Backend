import mongoose, { Schema } from "mongoose";
const servicesSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
export const Service = mongoose.model("Service", servicesSchema);
